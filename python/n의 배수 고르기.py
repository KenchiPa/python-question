# n의 배수 고르기 - 프로그래머스

n = 5
numlist = [1, 9, 3, 10, 13, 5]

def solution(n, numlist):
    
    a = [x for x in numlist if x % n == 0]
    print(a)
    return a

solution(n, numlist)

